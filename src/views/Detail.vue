import axios from 'axios';
import { useCartStore } from '@/stores/cart';
import { computed, ref } from 'vue';
import { useRoute } from 'vue-router';
import swal from "sweetalert2"
export default {
    name: 'Detail',
    setup() {
        const route = useRoute();
        const cartStore = useCartStore();
        const product = ref({});
        const formAddCart = ref({
            product: {},
            qty: 0,
            total_price: 0
        });

        const fetchProduct = async () => {
            try {
                const response = await axios.get(`https://dummyjson.com/products/${route.params.id}`);
                if (response.status !== 200) {
                    return alert('error');
                }
                product.value = response.data;
                formAddCart.value.qty = product.value.minimumOrderQuantity;
            } catch (error) {
                alert(error);
            }
        };

        const totalPrice = computed(() => parseFloat((formAddCart.value.qty * product.value.price).toFixed(2)));

        const addQuantity = () => {
            if (formAddCart.value.qty < product.value.stock) {
                formAddCart.value.qty += 1;
            }
        };

        const subQuantity = () => {
            if (formAddCart.value.qty !== 0 && formAddCart.value.qty > product.value.minimumOrderQuantity) {
                formAddCart.value.qty -= 1;
            }
        };

        const addToCart = () => {
            formAddCart.value.product = product.value;
            formAddCart.value.total_price = totalPrice.value;
            cartStore.addToCart(formAddCart.value);
            return swal.fire({
                icon: 'success',
                text: 'Success To Add Product'
            });
        };

        return {
            product,
            formAddCart,
            totalPrice,
            addQuantity,
            subQuantity,
            addToCart,
            fetchProduct
        };
    },
    mounted() {
        this.fetchProduct();
    }
};
